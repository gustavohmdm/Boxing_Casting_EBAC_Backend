
public class Boxing_Casting {
    public static void main(String[] args) {

        char letra1 = 'A';

        Character letra2 = letra1;

        System.out.println(letra2);

        int num1 = 1000000;

        System.out.println(num1);

        short num2 = (short) num1;

        System.out.println(num2);
    }
}